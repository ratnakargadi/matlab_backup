function func = fit_C(z)
z1 = find((z<=-50)&(z>=-94));
z2 = find((z<-94)&(z>=-141));
z3 = find((z<-141)&(z>=-164));
z4 = find((z<-164)&(z>=-200));
func = zeros(length(z(:)),1);
func(z1) = 4.7 * 10^-6 * z(z1).^2 + 0.7802 * 10^-3 * z(z1) + 28.2499 * 10^-3;
func(z2) = -1.685 * 10^-3 - 25.529 * 10^-6 * (z(z2) + 141);
func(z3) = -0.0129 * 10^-3 * z(z3).^2 - 4.0618 * 10^-3 * z(z3) - 317.9225 * 10^-3;
func(z4) = 1.3177 * 10^-4 + 0.0306 * 10^-3 * (z(z4) + 200);
end

